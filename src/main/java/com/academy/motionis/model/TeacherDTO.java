package com.academy.motionis.model;

public class TeacherDTO {
	private int t_no;
	private String t_code;
	private String t_name;
	private String t_edu;
	private String t_major;
	private String t_img;
	
	public String getT_code() {
		return t_code;
	}
	public void setT_code(String t_code) {
		this.t_code = t_code;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_edu() {
		return t_edu;
	}
	public void setT_edu(String t_edu) {
		this.t_edu = t_edu;
	}
	public String getT_img() {
		return t_img;
	}
	public void setT_img(String t_img) {
		this.t_img = t_img;
	}
	public int getT_no() {
		return t_no;
	}
	public void setT_no(int t_no) {
		this.t_no = t_no;
	}
	public String getT_major() {
		return t_major;
	}
	public void setT_major(String t_major) {
		this.t_major = t_major;
	}
	
	
	
}
